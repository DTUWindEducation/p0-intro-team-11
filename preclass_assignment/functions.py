def say_hello(name):
    """Return a greeting to the user."""
    return f"Hello {name}!"


def square(num):
    """Return the square of a number."""
    return num ** 2

def sqrt(num):
    """Return the square root of a number."""
    return num ** 0.5

def cube(num):
    """Return the cube of a number."""
    return num ** 3
